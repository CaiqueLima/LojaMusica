    private void validarLogin(){
        
       FuncionarioController fc = new FuncionarioController();
       
       
       
       String senha = txSenha.getText();
       String login = txLogin.getText();
       
       boolean retorno = fc.autenticarLogin(login, senha);
       
       if(retorno == true){
           
       JOptionPane.showMessageDialog(null, "Login efetuado com sucesso..!");
       
       JanelaPrincipal jp = new JanelaPrincipal();
       jp.setLocationRelativeTo(null);
       jp.setVisible(true);
       this.dispose();
       }
       
       else if(txSenha.getText().equals("") && txLogin.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Campos de texto vazio!");
       }   
       else if(txSenha.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Campo de Senha vazio!");
       }   
       else if(txLogin.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Campo de Login vazio!");
       }
       else {
           JOptionPane.showMessageDialog(null, "Campos incorretos!");
       }
        
    }

